package jiya;

public class MathLibs {

    public static void main(String[] args) {

        double a = 20.2;

        System.out.println("Square Root = " + Math.sqrt(a));
        System.out.println("Cube Root = " + Math.cbrt(a));
        System.out.println("Round Down = " + Math.floor(a));
        System.out.println("Round Up = " + Math.ceil(a));
        System.out.println("Absolute = " + Math.abs(a));
        System.out.println("Round Off = " + Math.round(a));
        System.out.println("Power of = " + Math.pow(a, 3));
        System.out.println("Ramdom = " + Math.random());

    }
}
